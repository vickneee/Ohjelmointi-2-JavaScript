const create = (title,caption,description,image) =>{
  const a = document.createElement('article');
  const dialog = document.querySelector('dialog');
  
  const h2 = document.createElement('h2');
  const t = document.createTextNode(title);
  h2.appendChild(t);
  a.appendChild(h2);
  
  const figure = document.createElement('figure');
  const figcap = document.createElement('figcaption');
  const cap = document.createTextNode(caption);
  figcap.appendChild(cap);
  const img = document.createElement('img');
  img.src = image.medium;
  img.alt = title;
  figure.appendChild(img);
  figure.appendChild(figcap);
  a.appendChild(figure);
  
  const p = document.createElement('p');
  const desc = document.createTextNode(description);
  p.appendChild(desc);
  a.appendChild(p);
  
  a.addEventListener('click',()=>{
    dialog.showModal()
    openImg(dialog,image.large,title)
  });
  
  return a;
}

const openImg = (dialog,src,title) =>{
  const img = dialog.querySelector('img')
  img.src = src
  img.alt = title
}

const closeImg =()=>{
  const dialog = document.querySelector('dialog');
  const span = dialog.querySelector('span');
  span.addEventListener('click', ()=>{
    dialog.close('close')
  })
}

const add = (arr) =>{
  const t = document.getElementById('pictures');
  let article;
  for (let a of arr){
    article = create(a.title, a.caption, a.description, a.image);
    t.appendChild(article);
  }
  closeImg()
}

add(picArray);
